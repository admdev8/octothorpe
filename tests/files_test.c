#include <string.h>

#include "oassert.h"

#include "files.h"

int main()
{
	char t1[128];
	char t2[128];

	split_fname("filename.ext", t1, sizeof(t1), t2, sizeof(t2));
	oassert(strcmp(t1, "filename")==0);
	oassert(strcmp(t2, "ext")==0);
	return 0;
};

